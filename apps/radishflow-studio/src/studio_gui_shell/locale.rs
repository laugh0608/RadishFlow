impl StudioShellLocale {
    pub(super) fn text(self, key: ShellText) -> &'static str {
        match self {
            StudioShellLocale::En => key.en(),
            StudioShellLocale::ZhCn => key.zh_cn(),
        }
    }

    pub(super) fn runtime_label<'a>(self, label: &'a str) -> Cow<'a, str> {
        if !matches!(self, StudioShellLocale::ZhCn) {
            return Cow::Borrowed(label);
        }

        match label {
            "Active" => Cow::Borrowed("活动"),
            "Hold" => Cow::Borrowed("暂停"),
            "Runnable" => Cow::Borrowed("可运行"),
            "Dirty" => Cow::Borrowed("待更新"),
            "Converged" => Cow::Borrowed("已收敛"),
            "Solving" => Cow::Borrowed("求解中"),
            "Checking" => Cow::Borrowed("检查中"),
            "Under-specified" => Cow::Borrowed("欠规格"),
            "Over-specified" => Cow::Borrowed("过规格"),
            "Unconverged" => Cow::Borrowed("未收敛"),
            "Error" => Cow::Borrowed("错误"),
            "Info" => Cow::Borrowed("信息"),
            "Warning" => Cow::Borrowed("警告"),
            "Text" => Cow::Borrowed("文本"),
            "Number" => Cow::Borrowed("数值"),
            "Choice" => Cow::Borrowed("选项"),
            "Synced" => Cow::Borrowed("已同步"),
            "Draft" => Cow::Borrowed("草稿"),
            "Valid" => Cow::Borrowed("有效"),
            "Invalid" => Cow::Borrowed("无效"),
            "Unit" => Cow::Borrowed("单元"),
            "Stream" => Cow::Borrowed("流股"),
            "Run" => Cow::Borrowed("运行"),
            "Resume" => Cow::Borrowed("恢复"),
            "Activate" | "Active mode" => Cow::Borrowed("活动模式"),
            "Hold mode" => Cow::Borrowed("暂停模式"),
            _ => Cow::Borrowed(label),
        }
    }

    pub(super) fn workspace_counts(
        self,
        flowsheet_name: &str,
        unit_count: usize,
        stream_count: usize,
        snapshot_count: usize,
    ) -> String {
        match self {
            StudioShellLocale::En => format!(
                "{flowsheet_name} | {unit_count} unit(s) | {stream_count} stream(s) | {snapshot_count} snapshot(s)"
            ),
            StudioShellLocale::ZhCn => format!(
                "{flowsheet_name} | {unit_count} 个单元 | {stream_count} 股流股 | {snapshot_count} 个快照"
            ),
        }
    }

    pub(super) fn solve_snapshot_counts(
        self,
        stream_count: usize,
        step_count: usize,
        diagnostic_count: usize,
    ) -> String {
        match self {
            StudioShellLocale::En => format!(
                "{stream_count} stream(s), {step_count} step(s), {diagnostic_count} diagnostic(s)"
            ),
            StudioShellLocale::ZhCn => {
                format!("{stream_count} 股流股，{step_count} 个步骤，{diagnostic_count} 条诊断")
            }
        }
    }

    pub(super) fn snapshot_identity(self, snapshot_id: &str, sequence: u64) -> String {
        match self {
            StudioShellLocale::En => format!("Snapshot {snapshot_id} seq {sequence}"),
            StudioShellLocale::ZhCn => format!("快照 {snapshot_id}，序号 {sequence}"),
        }
    }
}

